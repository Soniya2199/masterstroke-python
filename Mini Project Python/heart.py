from turtle import *
bgcolor('black')
pencolor('white')
fillcolor('red')
pensize(2)

def heart(f,c):
    begin_fill()
    lt(40)
    fd(f)
    circle(c,210)
    rt(138)
    circle(c,210)
    fd(f)
    end_fill()
penup()
goto(-50,-70)
pendown()
heart(140,70)
penup()
goto(70,70)
pendown()
left(40)
heart(100,50)

style=("times new roman",20,"bold",)
penup()
goto(-175,-150)
pendown()
write('ARAVINDHAN LOVES SONIYA',font=style,move=True,)
hideturtle()
done()
